  readData(operation: string, params?: string): Observable<any> {
    const cacheKey = `${operation}_${params || 'default'}`;

    // Check cache first
    const cachedData = this.getCachedData(cacheKey);
    if (cachedData) {
      return of(cachedData);
    }

    // If not in cache, fetch from API
    const url = `${environment.apiUrl}?action=${operation}${params ? `&${params}` : ''}`;

    return this.http.get(url).pipe(
      tap(data => {
        // Cache the response
        this.setCachedData(cacheKey, data);
      })
    );
  }

  createData(data: any): Observable<any> {
    return this.http.post<any>(
      `${this.apiUrl}?action=create`,
      JSON.stringify(data)
    );
  }

  updateData(
    payload: any,
    id: number,
    sheet: string = 'transaction'
  ): Observable<any> {
    const data = { values: payload, rowIndex: id, sheetName: sheet };
    return this.http.post<any>(
      `${this.apiUrl}?action=update`,
      JSON.stringify(data)
    );
  }

  deleteData(rowIndex: number, sheetName: string): Observable<any> {
    const data = { rowIndex, sheetName };
    return this.http.post<any>(
      `${this.apiUrl}?action=delete&rowIndex=${rowIndex}`,
      JSON.stringify(data)
    );
  }

  login(creds: any) {
    return this.http.post<any>(
      `${this.apiUrl}?action=login`,
      JSON.stringify(creds)
    );
  }

  private getCachedData(key: string): any | null {
    const cached = this.cache.get(key);
    if (cached && Date.now() - cached.timestamp < this.CACHE_DURATION) {
      return cached.data;
    }
    return null;
  }

  private setCachedData(key: string, data: any): void {
    this.cache.set(key, { data, timestamp: Date.now() });
  }

  // Cache invalidation methods
  clearCache(): void {
    console.log('üóëÔ∏è Clearing all cache');
    this.cache.clear();
  }

  clearCacheByPattern(pattern: string): void {
    console.log(`üóëÔ∏è Clearing cache for pattern: ${pattern}`);
    const keysToDelete: string[] = [];

    for (const key of this.cache.keys()) {
      if (key.includes(pattern)) {
        keysToDelete.push(key);
      }
    }

    keysToDelete.forEach(key => {
      this.cache.delete(key);
      console.log(`üóëÔ∏è Cleared cache key: ${key}`);
    });
  }

  clearTransactionCache(): void {
    console.log('üóëÔ∏è Clearing transaction-related cache');
    this.clearCacheByPattern('readTransactions');
  }


